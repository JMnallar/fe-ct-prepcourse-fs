function obtenerAreaRectangulo(alto, ancho) {
  // Retornar el área de un rectángulo teniendo su alto y ancho.
  // Tu código:
    // Paso 1: Calcular el área del rectángulo multiplicando su alto por su ancho
    let area = alto * ancho;

    // Paso 2: Retornar el área calculada
    return area;
}

